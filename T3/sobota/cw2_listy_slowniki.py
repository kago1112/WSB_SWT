owoce = ["banan", "jablko", "mandarynka"]

#slownik z cenami
ceny = {"banan": 4, "jablko": 2, "mandarynka": 5}

#wyswietlenie wynikow
ulubiony = owoce[2]
lubie = owoce[1]
nie_lubie = owoce[0]
print(f"Mój ulubiony owoc to {ulubiony} i kosztuje {ceny[ulubiony]} zł")
print(f"Lubie {lubie} i kosztuje {ceny[lubie]} zł")
print(f"Nie lubię {nie_lubie} i kosztuje {ceny[nie_lubie]} zł")