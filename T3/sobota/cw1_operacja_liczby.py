# pobranie danych
num1 = float(input("Podaj pierwsza liczbe: "))
num2 = float(input("Podaj druga liczbe: "))


#operacje na liczbach
suma = num1 + num2
roznica = num1 - num2
potegowanie = num1 ** num2
mnozenie = num1 * num2
dzielenie = num1 / num2
reszta = num1 % num2


#wyswietlenie wynikow
print(f"Suma Twoich liczb {num1} i {num2} wynosi {suma}")
print(f"Roznica Twoich liczb {num1} i {num2} wynosi {roznica}")
print(f"Potega Twoich liczb {num1} i {num2} wynosi {potegowanie}")
print(f"Mnozenie Twoich liczb {num1} i {num2} wynosi {mnozenie}")
print(f"Iloraz Twoich liczb {num1} i {num2} wynosi {dzielenie}")
print(f"Reszta z dzielenia Twoich liczb {num1} i {num2} wynosi {reszta}")